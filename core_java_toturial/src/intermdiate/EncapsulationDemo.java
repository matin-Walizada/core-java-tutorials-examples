package intermdiate;

public class EncapsulationDemo {
	
	// private fields 
	private int ssn;
	private String name;
	private int age;
	

	// getters and setters to access fields
	public int getSsn() {
		return ssn;
	}
	public void setSsn(int ssn) {
		this.ssn = ssn;
	}
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
	 
	
	
	
	
	
}
